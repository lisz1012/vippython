lst = [10, 20, 30, 40, 50, 60, 70, 80]
print(lst[1:6:1])  # [20, 30, 40, 50, 60], 这时个新的列表对象
print('原列表', id(lst))
print('切片后的列表', id(lst[1:6:1]))
print(lst[1:6])   # 默认步长是1
print(lst[1:6:2])  # [20, 40, 60], 步长是2
print(lst[:6:2])  # [10, 30, 50], 默认起始位置是0
print(lst[1::2])  # [20, 40, 60, 80], 默认结束位置是最后一个元素
# 步长为负数的情况
print(lst[6:1:-1])  # [], 步长为负数时, 起始位置必须大于结束位置
print(lst[7:0:-2])  # [80, 60, 40, 20], 步长为负数时, 起始位置必须大于结束位置
